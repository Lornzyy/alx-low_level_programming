#include "3-calc.h"


/**
 * op_add - addition function
 * @a: first int
 * @b: second int
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction function
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication function
 * @a: first int
 * @b: second int
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divide function
 * @a: first int
 * @b: second int
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to return modulo
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
